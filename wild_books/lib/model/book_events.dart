class SingleBookEvents {
  final String title;
  final String author;
  final String bookCover;
  final String event;
  final DateTime timestamp;
  final String nameOfEventStarter;
  final String note;
  final String comments;
  SingleBookEvents({
    required this.title,
    required this.author,
    required this.bookCover,
    required this.event,
    required this.timestamp,
    required this.nameOfEventStarter,
    required this.note,
    required this.comments,
  });
}
